namespace EntityFrameworkCore.Generator.Options
{
    /// <summary>
    /// Identifier naming strategy
    /// </summary>
    public enum IdentifierNaming
    {
        /// <summary>
        /// Use identifier name same as column name
        /// </summary>
        Preserve = 0,
        /// <summary>
        /// Use property name as column name
        /// </summary>
        Lower = 0,
        /// <summary>
        /// Use property name as column name
        /// </summary>
        Upper = 0
    }
}